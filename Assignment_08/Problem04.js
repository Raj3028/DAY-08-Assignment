// Find sum and multiplication of  all natural numbers from 10 to 20

//Find sum of  all natural numbers from 10 to 20:
let sum = 0;

for (let i = 10; i <= 20; i++) {
  sum += i;
}
console.log(sum);
//Output: 165
//
//
//Find multiplication of  all natural numbers from 10 to 20:
let multi = 1;

for (let i = 10; i <= 20; i++) {
  multi *= i;
}
console.log(multi);
//Output: 6704425728000
